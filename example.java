
class Example {

    public static void main(String args[]) {

        int x = 45;
        int y = 60;

        System.out.println(x);
        System.out.println(y);

        // swapping
        int temp = x;
        x = y;
        y = temp;

        System.out.println(x);
        System.out.println(y);

    }

}
